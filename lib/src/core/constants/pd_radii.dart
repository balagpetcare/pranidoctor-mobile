/// Corner radii aligned with [AppTheme] (cards 16, inputs/buttons 12).
abstract final class PdRadii {
  static const double xs = 8;
  static const double sm = 12;
  static const double md = 16;
  static const double lg = 20;

  /// Buttons, text fields (outline).
  static const double input = sm;

  /// Filled / outlined buttons.
  static const double button = sm;

  /// Cards and sheets.
  static const double card = md;

  /// Chips, small badges.
  static const double chip = xs;
}
